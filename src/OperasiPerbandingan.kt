/*
* Operasi Perbandingan
* Operasi perbandingan adalah operasi untuk membandingkan dua buah data
* Operasi perbandingan adalah operasi yang menghasilkan nilai boolean
* Jika operasinya adalah benar, maka nilainya true dan jika salah maka nilainya false
*/

fun main() {
    val a = 200
    val b = 200

    val result: Boolean = a == b
    println(result)

    println(100 >= 100)
    println(100 <= 100)

    println("a" < "b")
}